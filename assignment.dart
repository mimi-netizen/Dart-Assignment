void main() {
  // int data type
  int age = 25;
  print('Age: $age');

  // double data type
  double height = 1.75;
  print('Height: $height');

  // String data type
  String name = 'John Doe';
  print('Name: $name');

  // List data type
  List<int> numbers = [1, 2, 3, 4, 5];
  print('Numbers: $numbers');

  // Map data type
  Map<String, String> person = {
    'name': 'Jane Smith',
    'age': '30',
    'city': 'New York'
  };
  print('Person: $person');
}
